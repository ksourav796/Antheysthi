package com.hyva.restopos.rest.pojo;

import lombok.Data;

import java.util.List;

@Data
public class SchedularPojo {
    private Long id;
    private String franchise;
    private String franchiseEmail;
    private String academicYear;
    private List<DocumentPojo> documentPojoList;

    public String getFranchiseEmail() {
        return franchiseEmail;
    }

    public void setFranchiseEmail(String franchiseEmail) {
        this.franchiseEmail = franchiseEmail;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getFranchise() {
        return franchise;
    }

    public void setFranchise(String franchise) {
        this.franchise = franchise;
    }

    public String getAcademicYear() {
        return academicYear;
    }

    public void setAcademicYear(String academicYear) {
        this.academicYear = academicYear;
    }

    public List<DocumentPojo> getDocumentPojoList() {
        return documentPojoList;
    }

    public void setDocumentPojoList(List<DocumentPojo> documentPojoList) {
        this.documentPojoList = documentPojoList;
    }
}
